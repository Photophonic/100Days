# lists - [a,b,c]
fruits = ["apple", "banana", "cherry"]
print(fruits)
print(fruits[2])
# alter item in list at index
fruits[2] = 'orange'
print(fruits[2])
# add a new item to the end of the list
fruits.append('cherry')
print(fruits)
# extend the list with multiple items in a new list
fruits.extend(['watermellon', 'pineapple', 'corn'])
print(fruits)


