resources = {
    "water": 300,
    "milk": 200,
    "coffee": 100,
}

def resource_amount(order_ingredients):
    is_enough = True
    for item in order_ingredients:
        if order_ingredients[item] >= resources[item]:
            print(f"Sorry, not enough of {item} for the drink.")
            is_enough =  False
    return is_enough
